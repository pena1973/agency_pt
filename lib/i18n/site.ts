export const defaultSiteLocale: SiteLocale = "ru";

export function isSiteLocale(value: string): value is SiteLocale {
  return value === "pt" || value === "en" || value === "ru" || value === "uk";
}

const rentPeriodTranslations: Record<SiteLocale, string> = {
  pt: "mes",
  en: "month",
  ru: "месяц",
  uk: "місяць",
};

export function formatPropertyPrice(
  priceAmount: number,
  mode: "sale" | "rent",
  locale: SiteLocale
) {
  const formattedAmount = new Intl.NumberFormat(locale === "en" ? "en-US" : "ru-RU", {
    maximumFractionDigits: 0,
  }).format(Math.max(0, priceAmount));
  const price = `€${formattedAmount}`;

  return mode === "rent" ? `${price} / ${rentPeriodTranslations[locale]}` : price;
}

